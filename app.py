with tab4:
    st.header("Explainability & Interactive Prediction")

    st.subheader("Global Feature Importance (SHAP)")
    explainer = shap.TreeExplainer(xgb_model)
    X_scaled = scaler.transform(X)
    shap_values = explainer(X_scaled[:300])

    fig_summary, ax_summary = plt.subplots()
    shap.summary_plot(shap_values, X_scaled[:300], feature_names=feature_cols, show=False)
    st.pyplot(fig_summary)
    st.write("This summary plot ranks features by their overall impact on the model's predictions. The color indicates the feature value, showing how higher ages or the presence of comorbidities push the prediction towards higher mortality risk.")

    st.markdown("---")
    st.subheader("Interactive Patient Risk Assessment")

    col_input1, col_input2 = st.columns(2)
    with col_input1:
        input_age = st.slider("Age", 0, 100, 60)
        input_sex = st.selectbox("Sex (1=Female, 2=Male)", [1, 2])
        input_pneumonia = st.selectbox("Pneumonia (1=Yes, 2=No)", [1, 2])
        input_diabetes = st.selectbox("Diabetes (1=Yes, 2=No)", [1, 2])
    with col_input2:
        input_hypertension = st.selectbox("Hypertension (1=Yes, 2=No)", [1, 2])
        input_obesity = st.selectbox("Obesity (1=Yes, 2=No)", [1, 2])
        input_copd = st.selectbox("COPD (1=Yes, 2=No)", [1, 2])

    input_data = pd.DataFrame([[
        input_age, input_sex, input_pneumonia, input_diabetes,
        input_hypertension, input_obesity, input_copd
    ]], columns=feature_cols)

    input_scaled = scaler.transform(input_data)
    prob = xgb_model.predict_proba(input_scaled)[0][1]

    st.markdown(f"### Predicted Mortality Probability: **{prob:.2%}**")

    shap_val_single = explainer(input_scaled)
    shap_val_single.data = input_data.values
    shap_val_single.feature_names = feature_cols

    fig_waterfall, ax_waterfall = plt.subplots()
    shap.plots.waterfall(shap_val_single[0], show=False)
    st.pyplot(fig_waterfall)
    st.write("The waterfall plot breaks down this specific prediction. It illustrates exactly how each individual health factor mathematically increases or decreases the patient's baseline risk to arrive at the final probability.")