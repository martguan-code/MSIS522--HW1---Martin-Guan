# Configure the page
st.set_page_config(page_title="COVID-19 Risk Stratification", layout="wide")
st.title("COVID-19 Patient Outcomes & Risk Stratification")

# Load data and models (cached for performance)
@st.cache_data
def load_data():
    return pd.read_csv('covid_data_sample.csv').dropna()

@st.cache_resource
def load_models():
    scaler = joblib.load('scaler.joblib')
    xgb_model = joblib.load('xgb_model.joblib')
    with open('metrics_results.json', 'r') as f:
        metrics_data = json.load(f)
    return scaler, xgb_model, metrics_data

df = load_data()
scaler, xgb_model, metrics_data = load_models()

feature_cols = ['AGE', 'SEX', 'PNEUMONIA', 'DIABETES', 'HYPERTENSION', 'OBESITY', 'COPD']
X = df[feature_cols]
y = df['DEATH']

