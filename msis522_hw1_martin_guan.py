joblib.dump(rf.best_estimator_, 'rf_model.joblib')
joblib.dump(xgb.best_estimator_, 'xgb_model.joblib')
mlp.save('mlp_model.keras')

# Save model comparison metrics to load in the app
with open('metrics_results.json', 'w') as f:
    json.dump(metrics_results, f)

